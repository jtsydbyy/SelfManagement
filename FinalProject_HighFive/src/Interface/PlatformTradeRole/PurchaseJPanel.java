    private void spnQuantityStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_spnQuantityStateChanged
        // TODO add your handling code here:
        if((int)spnQuantity.getValue() <= fp.getQuantity()){
            double total = (int)spnQuantity.getValue() * fp.getPurchasePrice();
            lblTotal.setText(String.valueOf(total));
        }
        else{
            spnQuantity.setValue(fp.getQuantity());
            JOptionPane.showMessageDialog(null, "There are not enough Fund Products!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_spnQuantityStateChanged


