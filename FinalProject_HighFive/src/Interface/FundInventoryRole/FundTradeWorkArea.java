    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
          int selectedRow = tblManageFunds.getSelectedRow();
        if (selectedRow >= 0) {
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete the Fund Product?", "Warning", dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION) {
                FundProduct fp = (FundProduct) tblManageFunds.getValueAt(selectedRow, 0);
                fundProductDirectory.deleteFundProduct(fp);
                populateTable();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select one row!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

