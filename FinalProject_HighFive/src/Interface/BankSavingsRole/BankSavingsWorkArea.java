    private void btnProcessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessActionPerformed
        // TODO add your handling code here:
        int selectedRow = tabTrade.getSelectedRow();
        
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null,"Please select a row.","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Trade trade = (Trade)tabTrade.getValueAt(selectedRow, 0);
        if(trade.getStatus().equals("Completed")){
            JOptionPane.showMessageDialog(null,"This Trade has been finished.","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
        else{
            trade.setStatus("Processing");
            ProcessTradeJPanel ProcessTradeJPanel = new ProcessTradeJPanel(userProcessContainer, trade);
            userProcessContainer.add("ProcessTradeJPanel", ProcessTradeJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
        
    }//GEN-LAST:event_btnProcessActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tabTrade.getSelectedRow();
        if(selectedRow >= 0){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Do you want to Delete this Trade?","Warning",dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION){
                Trade t = (Trade)tabTrade.getValueAt(selectedRow, 0);
                organization.getTradeDirectory().deleteTrade(t);
                populateTable();
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"Please select a row.","Warning",JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_btnDeleteActionPerformed


