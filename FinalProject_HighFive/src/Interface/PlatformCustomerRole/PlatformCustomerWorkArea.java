    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tabCustomer.getModel();

        model.setRowCount(0);

        for (Customer c : enterprise.getCustomerdirectory()) {
            Object[] row = new Object[3];
            row[0] = c;
            row[1] = c.getUserid();
            row[2] = c.getAccount();
            model.addRow(row);
        }
 }
