    private void btn2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn2ActionPerformed
        // TODO add your handling code here:
        txtName.setText("");
        txtRP1.setText("");
        txtRP2.setText("");
        cmbType.setSelectedItem(null);
        cmbCompany.setSelectedItem(null);
        result = new ArrayList<>();
        if(Validation.IsLegalString(txtPP1.getText()) == true && Validation.IsLegalString(txtPP2.getText()) == true
                && Validation.IsDigit(txtPP1.getText()) == true && Validation.IsDigit(txtPP2.getText()) == true){
            for(FundProduct fp : allDirectory.getFundProductDirectory()){
            if(fp.getPurchasePrice() <= Double.parseDouble(txtPP2.getText()) && fp.getPurchasePrice()>= Double.parseDouble(txtPP1.getText())){
                result.add(fp);
            }
        }
        populateTable2();
        }
        else
        JOptionPane.showMessageDialog(null, "Please enter correct information!", "Warning", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_btn2ActionPerformed

