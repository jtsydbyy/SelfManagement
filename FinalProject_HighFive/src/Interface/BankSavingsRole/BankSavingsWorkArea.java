    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel) tabTrade.getModel();
        
        model.setRowCount(0);
        
        for (Trade trade : organization.getTradeDirectory().getTradeList()){
            //if(trade.getReceiver().equals(account)){
                Object[] row = new Object[4];
                row[0] = trade;
                row[1] = trade.getTradeType();
                row[2] = trade.getStatus();
                row[3] = trade.getRequestDate();
                model.addRow(row);
            //}
        }
    }
