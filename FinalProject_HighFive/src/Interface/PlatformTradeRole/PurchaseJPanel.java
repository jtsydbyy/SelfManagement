    private void btnPurchaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPurchaseActionPerformed
        // TODO add your handling code here:
        PurchaseTrade trade = new PurchaseTrade(Trade.TradeType.Purchase);
        trade.setAmount(lblTotal.getText());
        trade.setSender(account);
        trade.setStatus("Send");
        trade.setCustomer((Customer) cmbCustomer.getSelectedItem());
        trade.setFp(fp);
        trade.setQuantity((int)spnQuantity.getValue());
        String quan = String.valueOf((int)spnQuantity.getValue());
        
        fp.setQuantity(fp.getQuantity() - (int)spnQuantity.getValue());
        fp.setSales(fp.getSales() + (int)spnQuantity.getValue());
        
        Customer cus = (Customer) cmbCustomer.getSelectedItem();
        cus.getTradeList().getTradeList().add(trade);
        
        Enterprise en = null;
        Organization org = null;
        
        for(Network net : business.getNetworkList()){
            for(Enterprise e : net.getEnterpriseDirectory().getEnterpriseList()){
                if(e == cus.getBank()){
                    en = e;
                    break;
                }
            }
        }
        
        if(en != null){
        for (Organization organization : en.getOrganizationDirectory().getOrganizationList()){
            if (organization instanceof BankSavingsOrganization){
                org = organization;
                break;
            }
        }
        if (org!=null){
            org.getTradeDirectory().getTradeList().add(trade);
            account.getTradeDirectory().getTradeList().add(trade);
        }
        }
        //System.out.println(cus.getEmail());
        if(cus.getEmail() != null){
            try {
            
            SendMail.sendMessage("smtp.gmail.com", "highfivetradeplatform@gmail.com",  
                    "highfive123", cus.getEmail(), "Purchase Confirmation", 
                    "Dear " + cus.getName() +", you have purchased " + fp.getName() + " successfully. " + "Quantity: " + quan + ".",
                    "text/html;charset=gb2312");
            
            }catch(MessagingException e){
                //e.printStackTrace();
            }
        }
        
        JOptionPane.showMessageDialog(null, "Purchase successfully!", "Warning", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_btnPurchaseActionPerformed

