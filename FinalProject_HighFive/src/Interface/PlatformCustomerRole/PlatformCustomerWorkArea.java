    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tabCustomer.getSelectedRow();
        if(selectedRow >= 0){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Do you want to Delete this Customer Account?","Warning",dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION){
                Customer c = (Customer)tabCustomer.getValueAt(selectedRow, 0);
                enterprise.deleteCustomer(c);
                populateTable();
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"Please select a row.","Warning",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


