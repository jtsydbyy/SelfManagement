    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if(cou < 4){
            int selectedRow = tblManageFunds.getSelectedRow();
        if (selectedRow >= 0) {
                FundProduct fp = (FundProduct) tblManageFunds.getValueAt(selectedRow, 0);
                list.add(fp);
                cou++;
        } else {
            JOptionPane.showMessageDialog(null, "Please select one row!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        }
        else{
            JOptionPane.showMessageDialog(null, "There can only be 4 Fund Products in compare list at most!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnCompareActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCompareActionPerformed
        // TODO add your handling code here:
        if(list.size() > 1){
            CompareJPanel cjp = new CompareJPanel(userProcessContainer, list);
        userProcessContainer.add("CompareJPanel", cjp);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        }
        else{
            JOptionPane.showMessageDialog(null, "Please add at least 2 Fund Products to compare list!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        list.removeAll(list);
        cou = 0;
    }//GEN-LAST:event_btnCompareActionPerformed

