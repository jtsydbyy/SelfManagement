 public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblManageFunds.getModel();

        model.setRowCount(0);

        for (FundProduct fp : fundProductDirectory.getFundProductDirectory()) {
            Object[] row = new Object[9];
            row[0] = fp;
            row[1] = fp.getId();
            row[2] = fp.getDescription();
            row[3] = fp.getFundType();
            row[4] = fp.getPurchaseDate();
            row[5] = fp.getRedemptionDate();
            row[6] = fp.getPurchasePrice();
            row[7] = fp.getRedemptionPrice();
            row[8] = fp.getQuantity();
            
            model.addRow(row);
        }
 }
